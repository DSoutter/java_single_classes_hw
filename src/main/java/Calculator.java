public class Calculator {

    public Calculator(){

    };

    public int addNumbers(int num1, int num2){
        return num1 + num2;
    }

    public int subtractNumber(int num1, int num2){
        return num1 - num2;
    }

    public double divideNumber(double num1, double num2) {
        return num1 / num2;
    }

    public int multiplyNumbers(int num1, int num2) {
        return num1 * num2;
    }
}
